class Customer {
  final String name;
  final String address;

  const Customer({
    required this.name,
    required this.address,
  });
}


class Supplier {
  final String name;
  final String address;
  final String paymentInfo;

  const Supplier({
    required this.name,
    required this.address,
    required this.paymentInfo,
  });
}
